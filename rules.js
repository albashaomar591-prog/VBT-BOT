let حالة_الباقة = "شغالة";
let عداد_تنبيه_الباقة = 0;

async function تحقق_الباقة(client, النظام, رقم_المرسل, النص) {
    const fs = require('fs');
    const path = require('path');
    const ملف_الكونفج = path.join(__dirname, 'clients', النظام.اسم_المجلد, 'config.js');

    const اليوم = new Date();
    const تاريخ_الانتهاء = new Date(النظام.تاريخ_الانتهاء);

    // لو الباقة خلصت
    if (اليوم > تاريخ_الانتهاء) {

        // ابعت تنبيه مرة واحدة بس
        if (عداد_تنبيه_الباقة === 0) {
            عداد_تنبيه_الباقة = 1;

            // 1. تنبيه للمشرف
            await client.sendMessage(النظام.رقم_المشرف, `تم انتهاء باقة VBX الشهرية للشركة: ${النظام.الشركة}\nالرقم: ${النظام.الرقم_التعريفي}`);

            // 2. تنبيه للمهندس عمر
            await client.sendMessage(النظام.رقم_عمر_المهندس, `تم انتهاء باقة العميل: ${النظام.الشركة}\nرقم: ${النظام.الرقم_التعريفي}\nالموقع: ${النظام.الموقع}\nاتجدد؟ رد "اتجدد" لتجديد 30 يوم أوتوماتيك، أو رد "لا"`);
        }

        // 3. لو المهندس رد "اتجدد" جدد 30 يوم أوتوماتيك
        if (رقم_المرسل === النظام.رقم_عمر_المهندس && النص.trim() === "اتجدد") {
            const تاريخ_جديد = new Date();
            تاريخ_جديد.setDate(تاريخ_جديد.getDate() + 30);
            const تاريخ_جديد_نص = تاريخ_جديد.toISOString().split('T')[0];

            let محتوى_الكونفج = fs.readFileSync(ملف_الكونفج, 'utf8');
            محتوى_الكونفج = محتوى_الكونفج.replace(/تاريخ_الانتهاء:\s*".*"/, `تاريخ_الانتهاء: "${تاريخ_جديد_نص}"`);
            fs.writeFileSync(ملف_الكونفج, محتوى_الكونفج, 'utf8');

            await client.sendMessage(النظام.رقم_عمر_المهندس, `✅ تم التجديد 30 يوم\nتاريخ الانتهاء الجديد: ${تاريخ_جديد_نص}`);
            await client.sendMessage(النظام.رقم_المشرف, `الباقة اتجددت 30 يوم أوتوماتيك عن طريق المهندس`);
            عداد_تنبيه_الباقة = 0; // عشان لو خلصت تاني تنبه
            حالة_الباقة = "شغالة";
            return true;
        }

        // 4. لو المهندس رد "لا"
        if (رقم_المرسل === النظام.رقم_عمر_المهندس && النص.trim() === "لا") {
            await client.sendMessage(النظام.رقم_عمر_المهندس, `تمام، مستني موافقتك للتجديد`);
            return false;
        }

        // 5. اقفل البوت للكل عملاء ومشرف
        حالة_الباقة = "متوقف";
        return false;
    }

    حالة_الباقة = "شغالة";
    return true; // الباقة شغالة
}

